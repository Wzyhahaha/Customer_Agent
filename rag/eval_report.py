"""RAG evaluation report generator.

Reads eval result JSON and generates a Markdown report.
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path


def _pct(value: float) -> str:
    return f"{value:.1%}"


def _safe_load(path: Path) -> dict | None:
    if not path.exists():
        return None
    with path.open("r", encoding="utf-8") as f:
        return json.load(f)


def _build_metrics_table(baseline: dict | None, enhanced: dict | None) -> str:
    rows: list[str] = []
    rows.append("| Metric | Baseline | Enhanced |")
    rows.append("|---|---|---|")

    metrics = [
        ("Route Accuracy", "route_accuracy"),
        ("Question Recall@K", "question_recall"),
        ("Question Precision@K", "question_precision"),
        ("Question Hit@K", "question_hit"),
        ("Question F1@K", "question_f1"),
        ("Domain Recall@K", "domain_recall"),
        ("Domain Precision@K", "domain_precision"),
        ("Domain Hit@K", "domain_hit"),
        ("Domain F1@K", "domain_f1"),
        ("Joint Hit@K", "joint_hit"),
    ]

    for label, key in metrics:
        b_val = baseline.get(key) if baseline else None
        e_val = enhanced.get(key) if enhanced else None
        b_str = _pct(b_val) if isinstance(b_val, (int, float)) else "—"
        e_str = _pct(e_val) if isinstance(e_val, (int, float)) else "—"
        rows.append(f"| {label} | {b_str} | {e_str} |")

    return "\n".join(rows)


def _build_case_table(details: list[dict] | None, max_rows: int = 20) -> str:
    if not details:
        return "_No case details available._"

    rows: list[str] = []
    rows.append("| # | Query | Expected | Actual | Correct | Q-Recall | D-Recall |")
    rows.append("|---|---|---|---|---|---|---|")

    for item in details[:max_rows]:
        name = item.get("name", item.get("case_id", ""))
        query = str(item.get("query", ""))[:30]
        expected = item.get("expected_route", "")
        actual = item.get("actual_route", "")
        correct = "OK" if item.get("route_correct") else "X"
        q_recall = _pct(item.get("question_recall_at_k", 0))
        d_recall = _pct(item.get("domain_recall_at_k", 0))
        rows.append(f"| {name} | {query} | {expected} | {actual} | {correct} | {q_recall} | {d_recall} |")

    return "\n".join(rows)


def generate_report(
    baseline_path: str,
    enhanced_path: str,
    output_path: str,
    dataset_path: str | None = None,
) -> None:
    baseline = _safe_load(Path(baseline_path))
    enhanced = _safe_load(Path(enhanced_path))

    lines: list[str] = []
    lines.append("# RAG Evaluation Report")
    lines.append("")
    lines.append("## 1. Dataset")
    lines.append("")

    if dataset_path:
        dp = Path(dataset_path)
        if dp.exists():
            count = sum(1 for _ in dp.open("r", encoding="utf-8") if _.strip())
            lines.append(f"- Source: `{dataset_path}`")
            lines.append(f"- Total cases: {count}")
    else:
        lines.append("- Source: `data/eval/test_queries.jsonl`")
        lines.append("- See `docs/evaluation.md` for dataset format and distribution.")

    lines.append("")
    lines.append("## 2. Pipelines")
    lines.append("")
    lines.append("- **Baseline**: Typed retrieval with QueryRouter (rule-based) + single vector search per domain")
    lines.append("- **Enhanced**: Multi-stage pipeline (planned — QueryAnalyzer + QueryRewriter + RRF + Reranker)")
    lines.append("")
    lines.append("## 3. Overall Metrics")
    lines.append("")
    lines.append(_build_metrics_table(baseline, enhanced))
    lines.append("")
    lines.append("## 4. Case Details (Baseline)")
    lines.append("")
    baseline_details = baseline.get("details") if baseline else None
    lines.append(_build_case_table(baseline_details))
    lines.append("")

    if enhanced and enhanced.get("details"):
        lines.append("## 5. Case Details (Enhanced)")
        lines.append("")
        lines.append(_build_case_table(enhanced.get("details")))
        lines.append("")

    lines.append("## 6. Bad Case Analysis")
    lines.append("")
    lines.append("_Bad case taxonomy and root cause analysis will be added in P3-B._")
    lines.append("")
    lines.append("## 7. Next Steps")
    lines.append("")
    lines.append("- [ ] Complete enhanced pipeline implementation")
    lines.append("- [ ] Run ablation studies (no_rewrite, no_rerank, etc.)")
    lines.append("- [ ] Add bad case taxonomy and analysis")
    lines.append("- [ ] Expand dataset to 50+ cases")
    lines.append("")
    lines.append("---")
    lines.append("")
    lines.append("*Report generated by `rag/eval_report.py`.*")

    output = Path(output_path)
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text("\n".join(lines), encoding="utf-8")
    print(f"Report written to {output}")


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate RAG evaluation report")
    parser.add_argument("--baseline", default="reports/eval_results_baseline.json")
    parser.add_argument("--enhanced", default="reports/eval_results_enhanced.json")
    parser.add_argument("--output", default="reports/eval_report.md")
    parser.add_argument("--dataset", default="data/eval/test_queries.jsonl")
    args = parser.parse_args()

    generate_report(
        baseline_path=args.baseline,
        enhanced_path=args.enhanced,
        output_path=args.output,
        dataset_path=args.dataset,
    )


if __name__ == "__main__":
    main()
