def build_after_sales_message(province: str, issue_summary: str = "") -> str:
    province = (province or "").strip()
    message_parts = [
        "该问题建议您联系售后或保修服务处理。",
        f"可先转接人工服务，**电话：{HUMAN_SERVICE_PHONE}**。",
    ]

    capital_info = PROVINCE_CAPITAL_CBD_MAP.get(province)
    if capital_info:
        capital_city, cbd_candidates = capital_info
        selected_cbd = random.choice(cbd_candidates)
        service_address = f"{capital_city} 的 {selected_cbd} 附近售后服务网点"
        message_parts.append(f"如需线下售后保修，建议前往 **{service_address}** 咨询。")
    else:
        message_parts.append("暂未匹配到您所在省份的省会城市商圈信息，建议优先拨打人工服务电话咨询最近售后网点。")

    if issue_summary:
        message_parts.append(f"问题摘要：{issue_summary}")

    return "\n\n".join(message_parts)
